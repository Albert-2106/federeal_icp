def call_gemini(prompt, retry=True):
    model = get_working_model()
    url = (f"https://generativelanguage.googleapis.com/v1beta/models/"
           f"{model}:generateContent?key={GEMINI_API_KEY}")
    payload = {
        "contents": [{"parts": [{"text": prompt}]}],
        "generationConfig": {"temperature": 0.1, "maxOutputTokens": 4000},
    }
    r = requests.post(url, json=payload, timeout=90)

    if r.status_code == 429 and retry:
        print(f"    ⏳ Rate limited — waiting 65s …")
        time.sleep(65)
        return call_gemini(prompt, retry=False)

    if not r.ok:
        try:
            err = r.json()
            print(f"\n    !! Gemini {r.status_code}: {json.dumps(err, indent=2)}")
        except Exception:
            print(f"\n    !! Gemini {r.status_code}: {r.text[:300]}")
        r.raise_for_status()

    resp = r.json()
    candidates = resp.get("candidates", [])
    if not candidates:
        raise ValueError(f"No candidates returned: {resp}")
    if candidates[0].get("finishReason") == "SAFETY":
        raise ValueError("Blocked by safety filters")

    raw = candidates[0]["content"]["parts"][0]["text"].strip()
    raw = re.sub(r"^```(?:json)?\s*", "", raw)
    raw = re.sub(r"\s*```$", "", raw)
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        # Gemini truncated mid-string — find the last complete key-value pair and close the object
        last_comma = raw.rfind('",\n')
        if last_comma == -1:
            last_comma = raw.rfind('",')
        if last_comma != -1:
            repaired = raw[: last_comma + 1] + "\n}"
            try:
                return json.loads(repaired)
            except json.JSONDecodeError:
                pass
        raise


def enrich_row(row):
    name     = str(row.get("Company Name", "")).strip()
    website  = str(row.get("Website", "")).strip()
    segment  = str(row.get("Segment", "")).strip()
    products = str(row.get("What They Make", "")).strip()
    revenue  = str(row.get("Revenue Band", "")).strip()
    city     = str(row.get("City", "Hyderabad")).strip()

    if not name or name.lower() in ("nan",""):
        return EMPTY_ROW.copy()

    print(f"    Scraping  {website or '(no website)'} …")
    page_text, sources = (scrape_website(website)
                          if website and website.lower() not in ("nan","none","")
                          else ("", []))
    if not page_text:
        print(f"    [WARN] No text scraped — relying on search only")

    query = f"{name} Hyderabad pharma CEO founder revenue growth USFDA"
    print(f"    Searching '{query[:65]}…'")
    search_snip = duckduckgo_search(query)

    prompt = build_prompt(name, website, city, segment, products, revenue,
                          page_text, search_snip)
    print(f"    Analysing with Gemini …")
    try:
        result = call_gemini(prompt)
        src_field = result.get("Evidence Sources", "")
        all_srcs  = sources + [s.strip() for s in src_field.split(",") if s.strip()]
        seen, deduped = set(), []
        for s in all_srcs:
            if s not in seen:
                seen.add(s); deduped.append(s)
        result["Evidence Sources"] = "; ".join(deduped)
        return {c: result.get(c, "") for c in NEW_COLS}
    except Exception as e:
        print(f"    [ERROR] {e}")
        traceback.print_exc()
        row_err = EMPTY_ROW.copy()
        row_err["Verdict"]           = "Error"
        row_err["Verdict Reasoning"] = str(e)
        return row_err


