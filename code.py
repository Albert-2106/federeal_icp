def clean_url(url):
    url = url.strip()
    if not url.startswith("http"):
        url = "https://" + url
    return url

def extract_text(html):
    try:
        soup = BeautifulSoup(html, "lxml")
    except Exception:
        soup = BeautifulSoup(html, "html.parser")
    for tag in soup(["script","style","nav","footer","header","noscript","svg","iframe"]):
        tag.decompose()
    return " ".join(soup.stripped_strings)

def safe_get(url, session):
    for _ in range(2):
        try:
            r = session.get(url, headers=SCRAPE_HEADERS, timeout=TIMEOUT, allow_redirects=True)
            if r.status_code == 200:
                return r
        except requests.exceptions.SSLError:
            try:
                r = session.get(url.replace("https://","http://"), headers=SCRAPE_HEADERS,
                                timeout=TIMEOUT, allow_redirects=True)
                if r.status_code == 200:
                    return r
            except Exception:
                pass
        except Exception:
            pass
        time.sleep(1)
    return None

def scrape_website(url):
    sources, text = [], ""
    if not url or url.lower() in ("nan","none",""):
        return "", []
    session = requests.Session()
    base = clean_url(url)
    sources.append(base)
    r = safe_get(base, session)
    if r:
        text = extract_text(r.text)
    for slug in ("/about-us","/about","/company","/who-we-are","/leadership"):
        try:
            r2 = safe_get(urljoin(base, slug), session)
            if r2 and "text/html" in r2.headers.get("content-type",""):
                extra = extract_text(r2.text)
                if len(extra) > 300:
                    text += " " + extra
                    sources.append(urljoin(base, slug))
                    break
        except Exception:
            continue
    return text[:MAX_TEXT], sources

def duckduckgo_search(query):
    try:
        url = "https://html.duckduckgo.com/html/?q=" + quote(query)
        r = requests.get(url, headers=SCRAPE_HEADERS, timeout=TIMEOUT)
        soup = BeautifulSoup(r.text, "lxml")
        snippets = [el.get_text(" ", strip=True) for el in soup.select(".result__snippet")[:6]]
        if not snippets:
            snippets = [el.get_text(" ", strip=True)
                        for el in soup.select("[data-result='snippet']")[:6]]
        return " | ".join(snippets)[:MAX_SEARCH]
    except Exception as e:
        return f"[search failed: {e}]"

def build_prompt(name, website, city, segment, products, revenue, text, search):
    p = SCORING_PROMPT_TEMPLATE
    for k, v in [("__NAME__",name),("__WEBSITE__",website),("__CITY__",city),
                 ("__SEGMENT__",segment),("__PRODUCTS__",products),("__REVENUE__",revenue),
                 ("__TEXT__",text),("__SEARCH__",search)]:
        p = p.replace(k, str(v))
    return p

